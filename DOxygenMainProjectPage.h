/// \mainpage <b>Transportation Management System(TMS) Project</b>   
///
/// <b>The TMS solution prototype </b>is a system that allows <i>Omnicorp Shipping Handling and Transportation (OSHT)</i>
/// staff members to manage the order creation, managementand billing process for the company.
/// 
/// <li>Specific, high - level functional stories and workflows are as follows :</li>
/// 
/// a)Call for, take, and then plan orders from the Contract Marketplace.
/// 
/// b)Planning an order requires that the requested cargo be assigned to one or more available Carriers
/// 
/// c) Carriers provide the Trips necessary to deliver Orders to their destination.
/// 
/// d) Once scheduled, the TMS can simulate the passage of time allowing for updates on the Carrier’s
/// trucks to be requested from the Carrier Update System.
/// 
/// e) Once Orders are completed, the TMS system will compute the final bill for the Customer, and post
/// the invoice to the accounts system
